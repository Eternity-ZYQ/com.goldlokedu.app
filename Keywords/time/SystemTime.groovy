public class SystemTime {
	@Keyword
	public String get_system_time(){
		Date date = new Date()

		SimpleDateFormat df = new SimpleDateFormat('yy-MM-dd HH:mm:ss')

		String time = df.format(date)

		return time
	}





	@Keyword
	public String get_day_time(){
